// Створити функцію, для фільтрування товарів із тестового завдання
// за ключем і його значенням.
// Функція приймає масив товарів, параметр фільтрування та його значення
//  і повертатиме відфільтрований масив.
// Експортувати цю функцію для підключення в майбутньому.

function filter(input, parameter, value) {
  return input.filter((item) => item[parameter] === value);
}

module.exports = {
  filter,
};
